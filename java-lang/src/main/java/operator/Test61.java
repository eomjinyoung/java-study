package operator;

public class Test61 {
  public static void main(String[] args) {
    // 비트 이동 연산자
    System.out.println(12 << 3); // 12 << 3 = 12 * 2의 이동비트수 승
    // ...00000000000000000000000000001100 << 3
    // 00000000000000000000000000001100___
    // 00000000000000000000000000001100000 = 96, 오른쪽 빈자리는 0으로 채운다.

    System.out.println(12 >> 3); // 12 >> 3 = 12 / 2의 이동비트수 승
    // 00000000000000000000000000001100 >> 3
    // ___00000000000000000000000000001100
    // 00000000000000000000000000000001 = 1, 왼쪽 빈자리는 부호비트로 채운다.

    System.out.println(12 >>> 3); // 12 >>> 3
    // 00000000000000000000000000001100 >>> 3
    // ___00000000000000000000000000001100
    // 00000000000000000000000000000001 = 1, 왼쪽 빈자리는 0으로 채운다.

    System.out.println("--------------------------------");

    System.out.println(-12 << 3); // -12 << 3
    // ...11111111111111111111111111110100 << 3
    // 11111111111111111111111111110100___
    // 11111111111111111111111111110100000 = -96, 오른쪽 빈자리는 0으로 채운다.

    System.out.println(-12 >> 3);
    // 11111111_11111111_11111111_11110100 >> 3
    // ___11111111_11111111_11111111_11110100
    // 11111111111_11111111_11111111_11110 = -2, 왼쪽 빈자리는 부호비트로 채운다.

    System.out.println(-12 >>> 3);
    // 11111111_11111111_11111111_11110100 >> 3
    // ___11111111_11111111_11111111_11110100
    // 00011111111_11111111_11111111_11110 = ?, 왼쪽 빈자리는 0으로 채운다.
  }
}
