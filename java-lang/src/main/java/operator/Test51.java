package operator;

public class Test51 {
  public static void main(String[] args) {
    // 비트 연산자
    System.out.println(0b0011 & 0b0101); // 0001 = 1
    System.out.println(0b0011 | 0b0101); // 0111 = 7
    System.out.println(0b0011 ^ 0b0101); // 0110 = 6
    System.out.println(~0b0011); // 1100 = -4

  }
}
